/**
 * Get Nice class by number
 */
export function getNiceClass(classNumber: NiceClassNumber): NiceClass | undefined {
  return NICE_CLASSES.find(c => c.classNumber === classNumber);
}

/**
 * Get all goods classes (1-34)
 */
export function getGoodsClasses(): NiceClass[] {
  return NICE_CLASSES.filter(c => c.category === 'goods');
}

/**
 * Get all services classes (35-45)
 */
export function getServicesClasses(): NiceClass[] {
  return NICE_CLASSES.filter(c => c.category === 'services');
}

/**
 * Search Nice classes by keyword (searches in descriptions)
 */
export function searchNiceClasses(keyword: string): NiceClass[] {
  const lowerKeyword = keyword.toLowerCase();
  return NICE_CLASSES.filter(c =>
    c.shortDescriptionDE.toLowerCase().includes(lowerKeyword) ||
    c.shortDescriptionEN.toLowerCase().includes(lowerKeyword) ||
    c.fullDescriptionDE.toLowerCase().includes(lowerKeyword) ||
    c.fullDescriptionEN.toLowerCase().includes(lowerKeyword)
  );
}

/**
 * Get Nice class options for dropdown/select (DPMA format)
 */
export function getNiceClassOptions(): Array<{ value: NiceClassNumber; label: string }> {
  return NICE_CLASSES.map(c => ({
    value: c.classNumber,
    label: `${c.classCode}: ${c.shortDescriptionDE}`,
  }));
}

export default NICE_CLASSES;
